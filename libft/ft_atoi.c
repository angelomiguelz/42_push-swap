#include "libft.h"

long int	ft_atoi(const char *str)
{
	int		i;
	int		neg;
	long	res;

	i = 0;
	neg = 1;
	res = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		if (res * 10 < -2147483648 || res * 10 > 2147483647)
			return (21474836473);
		res = res * 10 + str[i] - 48;
		i++;
	}
	return (res * neg);
}
